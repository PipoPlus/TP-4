package ejericico3.Modelo;

public class Inscripcion {


    String apellido;
    String nombre;
    String telefono;
    String email;
    String concurso;
    int id;

    public Inscripcion(String apellido, String nombre, String telefono, String email, String concurso, int id) {
        this.apellido = apellido;
        this.nombre = nombre;
        this.telefono = telefono;
        this.email = email;
        this.concurso = concurso;
        this.id = id;
    }

}
